/**
 * @file memory_range_cast.h
 * @brief Макрос для приведения указателя к типу `ae_memory_range_t*`.
 *
 * Данный файл содержит макрос `ae_memory_range_cast`,
 * который выполняет приведение типа указателя к типу `ae_memory_range_t*`.
 *
 * Макрос может быть использован для безопасного приведения указателей,
 * когда вы уверены, что указатель указывает на объект соответствующего типа.
 */

#ifndef AE_MEMORY_RANGE_CAST_H
#define AE_MEMORY_RANGE_CAST_H

/**
 * @def ae_memory_range_cast(ptr, ...)
 * @brief Приводит указатель к типу `ae_memory_range_t*`.
 *
 * Данный макрос выполняет приведение типа указателя `ptr` к типу `ae_memory_range_t*`.
 * Он может использоваться для безопасного приведения указателей, когда вы уверены,
 * что указатель указывает на объект соответствующего типа.
 *
 * @param ptr Указатель, который необходимо привести к типу `ae_memory_range_t*`.
 * @param ... Опциональный квалификатор `const`, если требуется,
 *            для приведения к `const ae_memory_range_t*`.
 *
 * @return Приведенный указатель типа `ae_memory_range_t*`.
 *
 * @note Убедитесь, что указатель `ptr` действительно указывает на объект типа `ae_memory_range_t`
 *       или его производного типа, чтобы избежать неопределенного поведения.
 *
 * @warning Использование этого макроса с некорректным
 *          указателем может привести к ошибкам выполнения.
 */
#define ae_memory_range_cast(ptr, ...) (__VA_ARGS__ ae_memory_range_t *)ptr

#endif // AE_MEMORY_RANGE_CAST_H